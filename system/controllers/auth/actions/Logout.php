<?php

namespace auth\actions;

/**
 * Действие для безопасного выхода пользователя из системы
 * 
 * Реализует корректное завершение сессии с очисткой всех связанных данных.
 * Обеспечивает защиту от атак типа session fixation путем полного уничтожения
 * текущей сессии и создания новой чистой сессии.
 *
 */
class Logout extends AuthAction {
    /**
     * Основной метод выполнения процесса выхода из системы
     * 
     * Выполняет следующие шаги:
     * 1. Полное уничтожение текущей сессии пользователя
     * 2. Создание новой чистой сессии для предотвращения session fixation
     * 3. Отправка пользовательского уведомления об успешном выходе
     * 4. Перенаправление на главную страницу сайта
     * 
     * @return void
     * @throws \Exception При критических ошибках управления сессией
     * 
     */
    public function execute() {
        // Полное уничтожение текущей сессии пользователя
        // Удаляет все сессионные данные как на сервере, так и в клиентской cookie
        session_destroy();
        
        // Создание новой пустой сессии для предотвращения атак session fixation
        // Гарантирует, что старые сессионные данные не могут быть использованы
        session_start();
        
        // Пользовательское уведомление об успешном выходе из системы
        // Отображается на следующей загруженной странице
        \Notification::success('Вы успешно вышли из системы');
        
        // Перенаправление на главную страницу сайта
        // Предотвращает доступ к защищенным страницам после выхода
        $this->redirect(BASE_URL);
    }
}